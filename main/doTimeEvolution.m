w = 0.517231671970585*omg_aux(:,:,3) + 0.096059710526147*omg_aux(:,:,4) + 0.063692468666290*dt*L(:,:,4) + ...
    0.386708617503269*omg_aux(:,:,5) + 0.226007483236906*dt*L(:,:,5);
end
